class EnhancedNatiq:
    def __init__(self):
        self.knowledge = {
            "برنامه نویسی": {
                "پایتون": "پایتون یک زبان برنامه‌نویسی سطح بالا، تفسیری و همه‌منظوره است.",
                "جاوااسکریپت": "جاوااسکریپت برای توسعه وب استفاده می‌شود و در مرورگرها اجرا می‌شود.",
                "هوش مصنوعی": "برای هوش مصنوعی از پایتون، R و Julia استفاده می‌شود."
            },
            "تکنولوژی": {
                "بلاکچین": "فناوری دفتر کل توزیع‌شده برای ثبت تراکنش‌ها.",
                "متاورس": "دنیای مجازی سه‌بعدی که کاربران در آن تعامل می‌کنند.",
                "اینترنت اشیا": "اتصال دستگاه‌های فیزیکی به اینترنت."
            },
            "علم": {
                "فیزیک": "علم مطالعه ماده و انرژی و تعامل بین آنها.",
                "ریاضی": "علم مطالعه اعداد، کمیت‌ها، فضا و تغییرات.",
                "زیست شناسی": "علم مطالعه موجودات زنده و حیات."
            }
        }
    
    def answer(self, question):
        q = question.lower()
        
        # جستجوی موضوع
        for category, topics in self.knowledge.items():
            for topic, info in topics.items():
                if topic in q:
                    return f"{topic}: {info}"
        
        # پاسخ‌های عمومی
        responses = [
            "سوال خوبی است! می‌توانید در گوگل جستجو کنید: " + question,
            "من در حال یادگیری بیشتر در مورد '" + question + "' هستم.",
            "این موضوع نیاز به تحقیق بیشتری دارد.",
            "آیا سوال خاص‌تری دارید؟"
        ]
        return random.choice(responses)

